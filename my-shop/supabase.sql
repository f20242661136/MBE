create table products (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  title text not null,
  price numeric not null,
  image_url text not null,
  description text,
  category text,
  rating numeric default 4.5,
  review_count integer default 0,
  stock_quantity integer default 10,
  specifications jsonb default '{}',
  is_active boolean default true
);

create table orders (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  customer_name text not null,
  phone text not null,
  city text not null,
  address text not null,
  product_id bigint references products(id),
  product_title text,
  product_price numeric,
  quantity integer default 1,
  total_amount numeric,
  status text default 'Pending' -- Pending, Confirmed, Shipped, Delivered, Returned
);
insert into storage.buckets (id, name, public)
select 'images', 'images', true
where not exists (
  select 1 from storage.buckets where id = 'images'
);
alter table products enable row level security;
alter table orders enable row level security;
create policy "Public View Products"
on products for select
using (true);
create policy "Admin Manage Products"
on products for all
using (auth.role() = 'authenticated')
with check (auth.role() = 'authenticated');
create policy "Public Create Orders"
on orders for insert
with check (true);
create policy "Admin View Orders"
on orders for select
using (auth.role() = 'authenticated');
create policy "Admin Update Orders"
on orders for update
using (auth.role() = 'authenticated')
with check (auth.role() = 'authenticated');
create policy "Public View Images"
on storage.objects
for select
using (bucket_id = 'images');
create policy "Admin Upload Images"
on storage.objects
for insert
with check (bucket_id = 'images' and auth.role() = 'authenticated');
create policy "Admin Delete Images"
on storage.objects
for delete
using (auth.role() = 'authenticated');

